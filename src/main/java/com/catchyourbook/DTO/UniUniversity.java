package com.catchyourbook.DTO;

public class UniUniversity {
	String uniId;
	String uniLocation;
	String name;
	String uniState;
	String uniInfo;
	
	public String getUniId() {
		return uniId;
	}
	public void setUniId(String uniId) {
		this.uniId = uniId;
	}
	public String getUniLocation() {
		return uniLocation;
	}
	public void setUniLocation(String uniLocation) {
		this.uniLocation = uniLocation;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUniState() {
		return uniState;
	}
	public void setUniState(String uniState) {
		this.uniState = uniState;
	}
	public String getUniInfo() {
		return uniInfo;
	}
	public void setUniInfo(String uniInfo) {
		this.uniInfo = uniInfo;
	}
}
